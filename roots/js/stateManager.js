/* ==========================================================
   BINAH COMMAND CENTER
   STATE MANAGER
========================================================== */

const StateManager = {

    state: {}

};

/* ==========================================================
   SET STATE
========================================================== */

StateManager.set = function(key,value){

    this.state[key] = value;

};

/* ==========================================================
   GET STATE
========================================================== */

StateManager.get = function(key){

    return this.state[key];

};

/* ==========================================================
   HAS STATE
========================================================== */

StateManager.has = function(key){

    return this.state.hasOwnProperty(key);

};

/* ==========================================================
   CLEAR
========================================================== */

StateManager.clear = function(){

    this.state = {};

};

/* ==========================================================
   GET ALL STATE
========================================================== */

StateManager.getAll = function(){

    return this.state;

};

/* ==========================================================
   REMOVE STATE
========================================================== */

StateManager.remove = function(key){

    delete this.state[key];

};

/* ==========================================================
   COUNT STATES
========================================================== */

StateManager.count = function(){

    return Object.keys(

        this.state

    ).length;

};

/* ==========================================================
   KEYS
========================================================== */

StateManager.keys = function(){

    return Object.keys(

        this.state

    );

};

/* ==========================================================
   DEBUG
========================================================== */

StateManager.debug = function(){

    console.table(

        this.state

    );

};